# [문제 요약 - 모험가 길드]
# N명의 모험가가 있고, 각 모험가는 공포도 값을 가진다.
# 공포도가 X인 모험가는 반드시 X명 이상으로 구성된 그룹에 포함되어야
# 여행을 떠날 수 있다.
# 모든 모험가를 반드시 그룹에 포함시킬 필요는 없으며,
# 만들 수 있는 '여행 가능한 그룹'의 최대 개수를 구하는 문제이다.
#
# [핵심 아이디어]
# 1. 공포도를 오름차순으로 정렬한다.
# 2. 공포도가 낮은 모험가부터 하나씩 그룹에 포함시킨다.
# 3. 현재 그룹에 포함된 인원 수가
#    '현재 확인 중인 모험가의 공포도 이상'이 되면
#    하나의 그룹을 결성하고, 인원 수를 0으로 초기화한다.
#
# [이유]
# 공포도가 낮은 모험가부터 그룹을 만들수록
# 그룹 조건을 만족시키기 쉬워지고,
# 결과적으로 만들 수 있는 그룹 수가 최대가 된다.
#
# [예시]
# 공포도: 2 3 1 2 2
# 정렬 후: 1 2 2 2 3
# → (1,2,2) 그룹 1개
# → (2,3) 그룹 1개
# 결과: 2