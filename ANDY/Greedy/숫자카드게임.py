# [문제 요약 - 숫자 카드 게임]
# N x M 형태로 숫자 카드가 놓여 있다.
# 먼저 하나의 '행'을 선택한 뒤,
# 그 행에 포함된 카드 중에서 가장 작은 숫자 카드를 반드시 뽑아야 한다.
#
# 즉, 각 행마다 "최소값"이 하나씩 정해지며,
# 이 최소값들 중에서 가장 큰 값을 선택하는 것이 목표이다.
#
# [핵심 아이디어 (그리디)]
# 1. 각 행에서 가장 작은 수(min)를 구한다.
# 2. 그 값들 중에서 가장 큰 값(max)을 선택한다.
#
# [이유]
# 한 행을 선택하면 해당 행의 최소값을 반드시 뽑아야 하므로,
# 최종 결과는 "행별 최소값 중 최댓값"이 된다.
# 각 행에서의 최선의 선택(최소값 선택)이
# 전체 문제의 최적해로 이어지므로 그리디 알고리즘을 적용할 수 있다.
#
# [예시]
# 카드:
# 3 1 2
# 4 1 4
# 2 2 2
#
# 각 행의 최소값: 1, 1, 2
# 최종 선택: max(1, 1, 2) = 2