n, m = map(int,input().split() ) # n 은 행, m 은 열 

result = 0 

for i in range(n): # 행의 개수 만큼 반복 
    data = list(map(int,input().split()))  # 파이썬은 input () 하면 한줄만 받음 
    min_value = min(data)
    result = max(result, min_value)

print(result)

